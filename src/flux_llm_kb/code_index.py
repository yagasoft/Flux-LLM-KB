def is_code_like_path(path: str | Path) -> bool:
    file_path = Path(path)
    return file_path.suffix.lower() in CODE_LANGUAGE_EXTENSIONS or file_path.name.lower() in DEVELOPER_ARTIFACT_NAMES


def language_for_path(path: str | Path) -> str:
    file_path = Path(path)
    return DEVELOPER_ARTIFACT_NAMES.get(file_path.name.lower()) or CODE_LANGUAGE_EXTENSIONS.get(file_path.suffix.lower(), "text")


def parse_code_file(path: str | Path, *, root: str | Path | None = None, relative_path: str | None = None) -> CodeIndexResult:
    file_path = Path(path)
    if relative_path is None:
        root_path = Path(root).resolve() if root is not None else file_path.parent.resolve()
        relative_path = _relative_path(file_path, root_path)
    else:
        relative_path = _normalise_relative_path(relative_path)
    language = language_for_path(file_path)
    raw_text = read_text_with_bom(file_path)

    if language == "python":
        return _parse_python(raw_text, relative_path=relative_path, language=language)
    if language == "sql":
        return _parse_sql(raw_text, relative_path=relative_path, language=language)
    if language in {"javascript", "typescript"}:
        return _parse_javascript_like(raw_text, relative_path=relative_path, language=language)
    if language == "csharp":
        return _parse_csharp(raw_text, relative_path=relative_path, language=language)
    if language in {"html", "vue", "svelte", "astro", "razor"}:
        return _parse_frontend_markup(raw_text, relative_path=relative_path, language=language)
    if language in {"css", "scss", "sass", "less"}:
        return _parse_stylesheet(raw_text, relative_path=relative_path, language=language)
    if language == "notebook":
        return _parse_notebook(raw_text, relative_path=relative_path, language=language)
    if language in COMMON_LANGUAGE_SYMBOL_PATTERNS:
        return _parse_common_language(raw_text, relative_path=relative_path, language=language)
    if language in {"json", "jsonc", "json5", "yaml", "toml", "ini", "config", "xml", "graphql", "protobuf", "dockerfile", "make", "just", "taskfile", "requirements", "gomod", "gosum", "diff", "solution", "dotenv"}:
        return _parse_config_like(raw_text, relative_path=relative_path, language=language)
    return _fallback_result(raw_text, relative_path=relative_path, language=language)


def symbols_to_metadata(symbols: tuple[CodeSymbol, ...]) -> list[dict[str, Any]]:
    return [_symbol_metadata(symbol) for symbol in symbols]


def references_to_metadata(references: tuple[CodeReference, ...]) -> list[dict[str, Any]]:
    return [_reference_metadata(reference) for reference in references]


def _parse_python(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    try:
        tree = ast.parse(raw_text)
    except SyntaxError as exc:
        return _fallback_result(
            raw_text,
            relative_path=relative_path,
            language=language,
            diagnostics={"error_type": type(exc).__name__, "line": exc.lineno},
        )

    lines = raw_text.splitlines()
    chunks: list[CodeChunk] = []
    symbols: list[CodeSymbol] = []
    references: list[CodeReference] = []

    module_chunk = _chunk(
        raw_text,
        title=f"{relative_path}::module",
        relative_path=relative_path,
        language=language,
        symbol_kind="module",
        primary_symbol="module",
        relationship="definition",
        line_start=1,
        line_end=max(1, len(lines)),
        chunk_index=0,
    )
    chunks.append(module_chunk)
    references.extend(_python_references(tree, language=language, relative_path=relative_path, chunk_index=0, source_symbol=None))

    for node in tree.body:
        if isinstance(node, ast.ClassDef):
            class_name = node.name
            class_metadata = _python_class_metadata(node)
            chunk_index = len(chunks)
            chunks.append(
                _chunk_from_lines(
                    lines,
                    title=f"{relative_path}::{class_name}",
                    relative_path=relative_path,
                    language=language,
                    symbol_kind="class",
                    primary_symbol=class_name,
                    relationship="definition",
                    line_start=node.lineno,
                    line_end=getattr(node, "end_lineno", node.lineno),
                    chunk_index=chunk_index,
                    extra=class_metadata or None,
                )
            )
            symbols.append(
                _symbol(
                    node.name,
                    class_name,
                    "class",
                    language,
                    relative_path,
                    node.lineno,
                    getattr(node, "end_lineno", node.lineno),
                    chunk_index,
                    exported=not node.name.startswith("_"),
                    metadata=class_metadata or None,
                )
            )
            references.extend(
                _python_class_references(
                    node,
                    language=language,
                    relative_path=relative_path,
                    chunk_index=chunk_index,
                    source_symbol=class_name,
                )
            )
            references.extend(_python_references(node, language=language, relative_path=relative_path, chunk_index=chunk_index, source_symbol=class_name))
            for child in node.body:
                if isinstance(child, (ast.FunctionDef, ast.AsyncFunctionDef)):
                    qualified = f"{class_name}.{child.name}"
                    child_chunk_index = len(chunks)
                    chunks.append(
                        _chunk_from_lines(
                            lines,
                            title=f"{relative_path}::{qualified}",
                            relative_path=relative_path,
                            language=language,
                            symbol_kind="method",
                            primary_symbol=qualified,
                            relationship="definition",
                            line_start=child.lineno,
                            line_end=getattr(child, "end_lineno", child.lineno),
                            chunk_index=child_chunk_index,
                        )
                    )
                    symbols.append(
                        _symbol(
                            child.name,
                            qualified,
                            "method",
                            language,
                            relative_path,
                            child.lineno,
                            getattr(child, "end_lineno", child.lineno),
                            child_chunk_index,
                            parent_symbol=class_name,
                            exported=not child.name.startswith("_"),
                            signature=_python_signature(child),
                        )
                    )
                    references.extend(_python_references(child, language=language, relative_path=relative_path, chunk_index=child_chunk_index, source_symbol=qualified))
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            routes = _python_routes(node)
            symbol_kind = _python_function_symbol_kind(node)
            chunk_relationship = "test" if symbol_kind == "test" else "fixture" if symbol_kind == "fixture" else "definition"
            chunk_index = len(chunks)
            chunks.append(
                _chunk_from_lines(
                    lines,
                    title=f"{relative_path}::{node.name}",
                    relative_path=relative_path,
                    language=language,
                    symbol_kind=symbol_kind,
                    primary_symbol=node.name,
                    relationship=chunk_relationship,
                    line_start=node.lineno,
                    line_end=getattr(node, "end_lineno", node.lineno),
                    chunk_index=chunk_index,
                    extra={"routes": routes} if routes else None,
                )
            )
            symbols.append(
                _symbol(
                    node.name,
                    node.name,
                    symbol_kind,
                    language,
                    relative_path,
                    node.lineno,
                    getattr(node, "end_lineno", node.lineno),
                    chunk_index,
                    exported=not node.name.startswith("_"),
                    signature=_python_signature(node),
                    metadata={"routes": routes} if routes else None,
                )
            )
            references.extend(
                _python_references(
                    node,
                    language=language,
                    relative_path=relative_path,
                    chunk_index=chunk_index,
                    source_symbol=node.name,
                    source_kind=symbol_kind,
                )
            )
            if symbol_kind == "test":
                references.extend(_python_fixture_references(node, language=language, relative_path=relative_path, chunk_index=chunk_index))

    return _result(chunks, symbols, references, language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_sql(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    chunks: list[CodeChunk] = []
    symbols: list[CodeSymbol] = []
    statements = [part.strip() for part in raw_text.split(";") if part.strip()]
    line_offsets = _line_offsets(raw_text)
    relationship = "migration" if _is_migration_path(relative_path) else "definition"
    for statement in statements or [raw_text]:
        match = SQL_SYMBOL_RE.search(statement)
        symbol_kind = (match.group("kind").lower() if match else "statement")
        name = match.group("name").split(".")[-1] if match else f"statement_{len(chunks) + 1}"
        position = raw_text.find(statement)
        line_start = _line_for_offset(line_offsets, max(position, 0))
        line_end = line_start + max(0, statement.count("\n"))
        chunk_index = len(chunks)
        chunks.append(
            _chunk(
                statement,
                title=f"{relative_path}::{name}",
                relative_path=relative_path,
                language=language,
                symbol_kind=symbol_kind,
                primary_symbol=name,
                relationship=relationship,
                line_start=line_start,
                line_end=line_end,
                chunk_index=chunk_index,
            )
        )
        if match:
            symbols.append(_symbol(name, name, symbol_kind, language, relative_path, line_start, line_end, chunk_index))
    return _result(chunks, symbols, (), language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_javascript_like(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    chunks: list[CodeChunk] = []
    symbols: list[CodeSymbol] = []
    references: list[CodeReference] = []
    lines = raw_text.splitlines()
    module_chunk = _chunk(
        raw_text,
        title=f"{relative_path}::module",
        relative_path=relative_path,
        language=language,
        symbol_kind="module",
        primary_symbol="module",
        relationship="definition",
        line_start=1,
        line_end=max(1, len(lines)),
        chunk_index=0,
    )
    chunks.append(module_chunk)
    for match in JS_SYMBOL_RE.finditer(raw_text):
        name = match.group("name") or match.group("var")
        symbol_kind = "class" if match.group("kind") == "class" else "function"
        line_start = raw_text[: match.start()].count("\n") + 1
        line_end = min(len(lines), line_start + 20)
        chunk_index = len(chunks)
        routes = [route.group("route") for route in ROUTE_RE.finditer(raw_text[max(0, match.start() - 160) : match.end() + 240])]
        chunk_relationship = "route" if routes else "definition"
        chunks.append(
            _chunk_from_lines(
                lines,
                title=f"{relative_path}::{name}",
                relative_path=relative_path,
                language=language,
                symbol_kind=symbol_kind,
                primary_symbol=name,
                relationship=chunk_relationship,
                line_start=line_start,
                line_end=line_end,
                chunk_index=chunk_index,
                extra={"routes": routes} if routes else None,
            )
        )
        symbols.append(
            _symbol(
                name,
                name,
                symbol_kind,
                language,
                relative_path,
                line_start,
                line_end,
                chunk_index,
                exported=not name.startswith("_"),
                metadata={"routes": routes} if routes else None,
            )
        )
        references.extend(_javascript_call_references(raw_text, match=match, symbol_name=name, language=language, relative_path=relative_path, chunk_index=chunk_index))
    for route in ROUTE_RE.finditer(raw_text):
        references.append(
            CodeReference(
                relationship_kind="route",
                target=route.group("route"),
                language=language,
                path=relative_path,
                line_start=raw_text[: route.start()].count("\n") + 1,
                line_end=raw_text[: route.start()].count("\n") + 1,
                chunk_index=0,
                source_symbol=route.group("handler"),
                metadata={"method": route.group("method").upper()},
            )
        )
    return _result(chunks, symbols, references, language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_csharp(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    lines = raw_text.splitlines()
    line_offsets = _line_offsets(raw_text)
    chunks: list[CodeChunk] = [
        _chunk(
            raw_text,
            title=f"{relative_path}::module",
            relative_path=relative_path,
            language=language,
            symbol_kind="module",
            primary_symbol="module",
            relationship="definition",
            line_start=1,
            line_end=max(1, len(lines)),
            chunk_index=0,
        )
    ]
    symbols: list[CodeSymbol] = []
    references: list[CodeReference] = []

    namespace_match = CS_NAMESPACE_RE.search(raw_text)
    namespace_name = namespace_match.group("name") if namespace_match else ""
    if namespace_match:
        namespace_line = _line_for_offset(line_offsets, namespace_match.start())
        symbols.append(
            _symbol(
                namespace_name,
                namespace_name,
                "namespace",
                language,
                relative_path,
                namespace_line,
                namespace_line,
                0,
                exported=True,
            )
        )

    for using in CS_USING_RE.finditer(raw_text):
        line = _line_for_offset(line_offsets, using.start())
        references.append(
            CodeReference(
                relationship_kind="using",
                target=using.group("target"),
                language=language,
                path=relative_path,
                line_start=line,
                line_end=line,
                chunk_index=0,
                confidence=0.95,
            )
        )

    class_infos: list[dict[str, Any]] = []
    for match in CS_TYPE_RE.finditer(raw_text):
        name = match.group("name")
        kind = match.group("kind").lower()
        line_start = _line_for_offset(line_offsets, match.start())
        line_end = _csharp_symbol_line_end(lines, line_start)
        qualified = f"{namespace_name}.{name}" if namespace_name else name
        attrs = _csharp_attributes_before(lines, line_start)
        route_prefixes = _csharp_routes_from_attrs(attrs, class_route=True)
        chunk_index = len(chunks)
        chunks.append(
            _chunk_from_lines(
                lines,
                title=f"{relative_path}::{qualified}",
                relative_path=relative_path,
                language=language,
                symbol_kind=kind,
                primary_symbol=qualified,
                relationship="definition",
                line_start=line_start,
                line_end=line_end,
                chunk_index=chunk_index,
                extra={"routes": route_prefixes} if route_prefixes else None,
            )
        )
        symbols.append(
            _symbol(
                name,
                qualified,
                kind,
                language,
                relative_path,
                line_start,
                line_end,
                chunk_index,
                parent_symbol=namespace_name or None,
                exported=_common_symbol_exported(raw_text, match.start(), name),
                metadata={"routes": route_prefixes} if route_prefixes else None,
            )
        )
        class_infos.append(
            {
                "name": name,
                "qualified": qualified,
                "line_start": line_start,
                "line_end": line_end,
                "routes": route_prefixes,
            }
        )

    for match in CS_METHOD_RE.finditer(raw_text):
        name = match.group("name")
        if name in {"if", "for", "foreach", "while", "switch", "catch"}:
            continue
        line_start = _line_for_offset(line_offsets, match.start())
        parent = _csharp_parent_class(class_infos, line_start)
        attrs = _csharp_attributes_before(lines, line_start)
        symbol_kind = "test" if CS_TEST_ATTR_RE.search("\n".join(attrs)) else "method"
        qualified = f"{parent['qualified']}.{name}" if parent else f"{namespace_name}.{name}" if namespace_name else name
        routes = _combine_csharp_routes(parent.get("routes", []) if parent else [], _csharp_routes_from_attrs(attrs, class_route=False))
        line_end = _csharp_symbol_line_end(lines, line_start)
        chunk_index = len(chunks)
        chunks.append(
            _chunk_from_lines(
                lines,
                title=f"{relative_path}::{qualified}",
                relative_path=relative_path,
                language=language,
                symbol_kind=symbol_kind,
                primary_symbol=qualified,
                relationship="route" if routes else "test" if symbol_kind == "test" else "definition",
                line_start=line_start,
                line_end=line_end,
                chunk_index=chunk_index,
                extra={"routes": routes} if routes else None,
            )
        )
        symbols.append(
            _symbol(
                name,
                qualified,
                symbol_kind,
                language,
                relative_path,
                line_start,
                line_end,
                chunk_index,
                parent_symbol=parent["qualified"] if parent else namespace_name or None,
                exported=_common_symbol_exported(raw_text, match.start(), name),
                metadata={"routes": routes} if routes else None,
            )
        )
        for route in routes:
            references.append(
                CodeReference(
                    relationship_kind="route",
                    target=route,
                    language=language,
                    path=relative_path,
                    line_start=line_start,
                    line_end=line_start,
                    chunk_index=chunk_index,
                    source_symbol=qualified,
                    confidence=0.95,
                    metadata={"framework": "aspnet"},
                )
            )
        method_body = "\n".join(lines[max(0, line_start - 1) : max(line_start, line_end)])
        for call in CS_CALL_RE.finditer(method_body):
            target = call.group("target")
            if target == name or target in CS_CALL_SKIP or target == match.group("return_type"):
                continue
            references.append(
                CodeReference(
                    relationship_kind="call",
                    target=target,
                    language=language,
                    path=relative_path,
                    line_start=line_start + method_body[: call.start()].count("\n"),
                    line_end=line_start + method_body[: call.start()].count("\n"),
                    chunk_index=chunk_index,
                    source_symbol=qualified,
                    confidence=0.7,
                )
            )

    if not symbols:
        return _fallback_result(raw_text, relative_path=relative_path, language=language)
    return _result(chunks, symbols, references, language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_frontend_markup(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    lines = raw_text.splitlines()
    component_name = _frontend_component_name(raw_text, relative_path, language)
    chunks = [
        _chunk(
            raw_text,
            title=f"{relative_path}::{component_name}",
            relative_path=relative_path,
            language=language,
            symbol_kind="component",
            primary_symbol=component_name,
            relationship="definition",
            line_start=1,
            line_end=max(1, len(lines)),
            chunk_index=0,
        )
    ]
    symbols = [
        _symbol(
            component_name,
            component_name,
            "component",
            language,
            relative_path,
            1,
            max(1, len(lines)),
            0,
            exported=True,
        )
    ]
    references: list[CodeReference] = []
    for pattern, relationship, target_group in (
        (FRONTEND_IMPORT_RE, "import", "target"),
        (FRONTEND_COMPONENT_TAG_RE, "component", "name"),
        (FRONTEND_ACTION_RE, "form_action", "target"),
    ):
        for match in pattern.finditer(raw_text):
            references.append(
                CodeReference(
                    relationship_kind=relationship,
                    target=match.group(target_group),
                    language=language,
                    path=relative_path,
                    line_start=raw_text[: match.start()].count("\n") + 1,
                    line_end=raw_text[: match.start()].count("\n") + 1,
                    chunk_index=0,
                    source_symbol=component_name,
                )
            )
    for match in FRONTEND_EVENT_RE.finditer(raw_text):
        references.append(
            CodeReference(
                relationship_kind="event",
                target=match.group("handler"),
                language=language,
                path=relative_path,
                line_start=raw_text[: match.start()].count("\n") + 1,
                line_end=raw_text[: match.start()].count("\n") + 1,
                chunk_index=0,
                source_symbol=component_name,
                metadata={"event": match.group("event")},
            )
        )
    references.extend(_html_selector_references(raw_text, language=language, relative_path=relative_path, source_symbol=component_name))
    return _result(chunks, symbols, references, language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_stylesheet(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    lines = raw_text.splitlines()
    chunk = _chunk(
        raw_text,
        title=f"{relative_path}::stylesheet",
        relative_path=relative_path,
        language=language,
        symbol_kind="stylesheet",
        primary_symbol=Path(relative_path).name,
        relationship="definition",
        line_start=1,
        line_end=max(1, len(lines)),
        chunk_index=0,
    )
    symbols: list[CodeSymbol] = []
    references: list[CodeReference] = []
    seen_symbols: set[tuple[str, str]] = set()
    for pattern, symbol_kind, prefix in (
        (CSS_CLASS_RE, "selector", "."),
        (CSS_ID_RE, "selector", "#"),
        (CSS_CUSTOM_PROPERTY_RE, "custom_property", ""),
        (CSS_KEYFRAMES_RE, "keyframes", ""),
    ):
        for match in pattern.finditer(raw_text):
            name = f"{prefix}{match.group('name')}"
            key = (symbol_kind, name)
            if key in seen_symbols:
                continue
            seen_symbols.add(key)
            line = raw_text[: match.start()].count("\n") + 1
            symbols.append(_symbol(name, name, symbol_kind, language, relative_path, line, line, 0, exported=True))
    for media in CSS_MEDIA_RE.finditer(raw_text):
        line = raw_text[: media.start()].count("\n") + 1
        references.append(
            CodeReference(
                relationship_kind="media_query",
                target=media.group("query").strip(),
                language=language,
                path=relative_path,
                line_start=line,
                line_end=line,
                chunk_index=0,
                source_symbol=Path(relative_path).name,
            )
        )
    return _result([chunk], symbols, references, language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_common_language(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    lines = raw_text.splitlines()
    line_offsets = _line_offsets(raw_text)
    chunks: list[CodeChunk] = []
    symbols: list[CodeSymbol] = []
    for symbol_kind, group_name, pattern in COMMON_LANGUAGE_SYMBOL_PATTERNS[language]:
        for match in pattern.finditer(raw_text):
            name = match.group(group_name)
            line_start = _line_for_offset(line_offsets, match.start())
            line_end = _common_symbol_line_end(lines, line_start)
            chunk_index = len(chunks)
            chunks.append(
                _chunk_from_lines(
                    lines,
                    title=f"{relative_path}::{name}",
                    relative_path=relative_path,
                    language=language,
                    symbol_kind=symbol_kind,
                    primary_symbol=name,
                    relationship="definition",
                    line_start=line_start,
                    line_end=line_end,
                    chunk_index=chunk_index,
                )
            )
            symbols.append(_symbol(name, name, symbol_kind, language, relative_path, line_start, line_end, chunk_index, exported=_common_symbol_exported(raw_text, match.start(), name)))
    if not chunks:
        return _fallback_result(raw_text, relative_path=relative_path, language=language)
    return _result(chunks, symbols, (), language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


def _parse_notebook(raw_text: str, *, relative_path: str, language: str) -> CodeIndexResult:
    try:
        payload = json.loads(raw_text)
    except json.JSONDecodeError as exc:
        return _fallback_result(
            raw_text,
            relative_path=relative_path,
            language=language,
            diagnostics={"error_type": type(exc).__name__, "line": exc.lineno},
        )
    cells = payload.get("cells") if isinstance(payload, dict) else None
    if not isinstance(cells, list):
        return _fallback_result(
            raw_text,
            relative_path=relative_path,
            language=language,
            diagnostics={"error_type": "InvalidNotebook", "reason": "missing cells"},
        )
    chunks: list[CodeChunk] = []
    symbols: list[CodeSymbol] = []
    line_cursor = 1
    for index, cell in enumerate(cells):
        if not isinstance(cell, dict):
            continue
        source = cell.get("source", "")
        text = "".join(source) if isinstance(source, list) else str(source or "")
        cell_type = str(cell.get("cell_type") or "cell")
        line_count = max(1, len(text.splitlines()))
        primary_symbol = f"{cell_type}_cell_{index}"
        chunk_index = len(chunks)
        chunks.append(
            _chunk(
                text,
                title=f"{relative_path}::{primary_symbol}",
                relative_path=relative_path,
                language=language,
                symbol_kind="notebook_cell",
                primary_symbol=primary_symbol,
                relationship="definition",
                line_start=line_cursor,
                line_end=line_cursor + line_count - 1,
                chunk_index=chunk_index,
                extra={"cell_type": cell_type, "cell_index": index},
            )
        )
        symbols.append(
            _symbol(
                primary_symbol,
                primary_symbol,
                "notebook_cell",
                language,
                relative_path,
                line_cursor,
                line_cursor + line_count - 1,
                chunk_index,
                metadata={"cell_type": cell_type, "cell_index": index},
            )
        )
        line_cursor += line_count
    return _result(chunks, symbols, (), language=language, parser_status="parsed", generated=_is_generated_file(raw_text))


