def _python_signature(node: ast.FunctionDef | ast.AsyncFunctionDef) -> str:
    names = [arg.arg for arg in node.args.posonlyargs + node.args.args + node.args.kwonlyargs]
    if node.args.vararg:
        names.append(f"*{node.args.vararg.arg}")
    if node.args.kwarg:
        names.append(f"**{node.args.kwarg.arg}")
    return f"{node.name}({', '.join(names)})"


def _python_routes(node: ast.FunctionDef | ast.AsyncFunctionDef) -> list[str]:
    routes: list[str] = []
    for decorator in node.decorator_list:
        if not isinstance(decorator, ast.Call):
            continue
        func = decorator.func
        if not isinstance(func, ast.Attribute) or func.attr not in {"get", "post", "put", "patch", "delete"}:
            continue
        if decorator.args and isinstance(decorator.args[0], ast.Constant) and isinstance(decorator.args[0].value, str):
            routes.append(decorator.args[0].value)
    return routes


def _python_function_symbol_kind(node: ast.FunctionDef | ast.AsyncFunctionDef) -> str:
    if _has_pytest_fixture_decorator(node):
        return "fixture"
    if node.name.startswith("test_"):
        return "test"
    return "function"


def _python_class_metadata(node: ast.ClassDef) -> dict[str, Any]:
    metadata: dict[str, Any] = {}
    bases = [_python_target_name(base) for base in node.bases]
    decorators = [_python_target_name(decorator.func if isinstance(decorator, ast.Call) else decorator) for decorator in node.decorator_list]
    bases = [base for base in bases if base]
    decorators = [decorator for decorator in decorators if decorator]
    if bases:
        metadata["bases"] = bases
    if decorators:
        metadata["decorators"] = decorators
    return metadata


def _python_class_references(
    node: ast.ClassDef,
    *,
    language: str,
    relative_path: str,
    chunk_index: int,
    source_symbol: str,
) -> list[CodeReference]:
    references: list[CodeReference] = []
    for base in node.bases:
        target = _python_target_name(base)
        if target:
            references.append(
                CodeReference(
                    relationship_kind="inherits",
                    target=target,
                    language=language,
                    path=relative_path,
                    line_start=getattr(base, "lineno", node.lineno),
                    line_end=getattr(base, "end_lineno", getattr(base, "lineno", node.lineno)),
                    chunk_index=chunk_index,
                    source_symbol=source_symbol,
                    confidence=1.0,
                )
            )
    for decorator in node.decorator_list:
        target = _python_target_name(decorator.func if isinstance(decorator, ast.Call) else decorator)
        if target:
            references.append(
                CodeReference(
                    relationship_kind="decorator",
                    target=target,
                    language=language,
                    path=relative_path,
                    line_start=getattr(decorator, "lineno", node.lineno),
                    line_end=getattr(decorator, "end_lineno", getattr(decorator, "lineno", node.lineno)),
                    chunk_index=chunk_index,
                    source_symbol=source_symbol,
                    confidence=1.0,
                )
            )
    return _dedupe_references(references)


def _has_pytest_fixture_decorator(node: ast.FunctionDef | ast.AsyncFunctionDef) -> bool:
    for decorator in node.decorator_list:
        func = decorator.func if isinstance(decorator, ast.Call) else decorator
        if isinstance(func, ast.Name) and func.id == "fixture":
            return True
        if isinstance(func, ast.Attribute) and func.attr == "fixture":
            return True
    return False


def _python_references(
    node: ast.AST,
    *,
    language: str,
    relative_path: str,
    chunk_index: int,
    source_symbol: str | None,
    source_kind: str | None = None,
) -> list[CodeReference]:
    references: list[CodeReference] = []
    for child in ast.walk(node):
        if isinstance(child, ast.ImportFrom):
            module = child.module or ""
            for alias in child.names:
                target = f"{module}.{alias.name}" if module else alias.name
                references.append(
                    CodeReference(
                        relationship_kind="import",
                        target=target,
                        language=language,
                        path=relative_path,
                        line_start=child.lineno,
                        line_end=child.lineno,
                        chunk_index=chunk_index,
                        source_symbol=source_symbol,
                        confidence=1.0,
                    )
                )
        elif isinstance(child, ast.Import):
            for alias in child.names:
                references.append(
                    CodeReference(
                        relationship_kind="import",
                        target=alias.name,
                        language=language,
                        path=relative_path,
                        line_start=child.lineno,
                        line_end=child.lineno,
                        chunk_index=chunk_index,
                        source_symbol=source_symbol,
                        confidence=1.0,
                    )
                )
        elif isinstance(child, ast.Call):
            target = _call_target(child.func)
            if target:
                references.append(
                    CodeReference(
                        relationship_kind="test" if source_kind == "test" else "call",
                        target=target,
                        language=language,
                        path=relative_path,
                        line_start=getattr(child, "lineno", 1),
                        line_end=getattr(child, "end_lineno", getattr(child, "lineno", 1)),
                        chunk_index=chunk_index,
                        source_symbol=source_symbol,
                    )
                )
    return _dedupe_references(references)


def _python_target_name(node: ast.AST) -> str | None:
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        base = _python_target_name(node.value)
        return f"{base}.{node.attr}" if base else node.attr
    if isinstance(node, ast.Call):
        return _python_target_name(node.func)
    if isinstance(node, ast.Subscript):
        return _python_target_name(node.value)
    return None


def _python_fixture_references(
    node: ast.FunctionDef | ast.AsyncFunctionDef,
    *,
    language: str,
    relative_path: str,
    chunk_index: int,
) -> list[CodeReference]:
    return [
        CodeReference(
            relationship_kind="fixture",
            target=arg.arg,
            language=language,
            path=relative_path,
            line_start=node.lineno,
            line_end=getattr(node, "end_lineno", node.lineno),
            chunk_index=chunk_index,
            source_symbol=node.name,
            confidence=1.0,
        )
        for arg in node.args.args
        if arg.arg != "self"
    ]


def _javascript_call_references(
    raw_text: str,
    *,
    match: re.Match[str],
    symbol_name: str,
    language: str,
    relative_path: str,
    chunk_index: int,
) -> list[CodeReference]:
    snippet = raw_text[match.end() : match.end() + 500]
    references: list[CodeReference] = []
    for call in JS_CALL_RE.finditer(snippet):
        target = call.group("target")
        if target == symbol_name or target in JS_CALL_SKIP:
            continue
        offset = match.end() + call.start()
        line = raw_text[:offset].count("\n") + 1
        references.append(
            CodeReference(
                relationship_kind="call",
                target=target,
                language=language,
                path=relative_path,
                line_start=line,
                line_end=line,
                chunk_index=chunk_index,
                source_symbol=symbol_name,
            )
        )
    return _dedupe_references(references)


def _common_symbol_line_end(lines: list[str], line_start: int) -> int:
    if not lines:
        return 1
    return min(len(lines), max(line_start, line_start + 20))


def _common_symbol_exported(raw_text: str, start: int, name: str) -> bool | None:
    prefix = raw_text[max(0, start - 32) : start].lower()
    return bool("pub " in prefix or "public " in prefix or "export " in prefix or name[:1].isupper())


def _call_target(func: ast.AST) -> str | None:
    if isinstance(func, ast.Name):
        return func.id
    if isinstance(func, ast.Attribute):
        base = _call_target(func.value)
        return f"{base}.{func.attr}" if base else func.attr
    return None


def _dedupe_references(references: list[CodeReference]) -> list[CodeReference]:
    seen: set[tuple[str, str, int, str | None]] = set()
    result: list[CodeReference] = []
    for reference in references:
        key = (reference.relationship_kind, reference.target, reference.line_start, reference.source_symbol)
        if key in seen:
            continue
        seen.add(key)
        result.append(reference)
    return result


def _line_offsets(text: str) -> list[int]:
    offsets = [0]
    for match in re.finditer("\n", text):
        offsets.append(match.end())
    return offsets


def _line_for_offset(offsets: list[int], offset: int) -> int:
    line = 1
    for index, value in enumerate(offsets, start=1):
        if value > offset:
            break
        line = index
    return line


def _relative_path(path: Path, root: Path) -> str:
    try:
        resolved = path.resolve()
        return resolved.relative_to(root).as_posix()
    except (OSError, ValueError):
        return path.name


def _normalise_relative_path(path: str) -> str:
    normalised = str(path).replace("\\", "/").strip("/")
    return normalised or Path(path).name


def scope_hash(root_name: str | None, path: str) -> str:
    return hashlib.sha256(f"{root_name or ''}:{path}".encode("utf-8")).hexdigest()[:16]
