def _is_migration_path(relative_path: str) -> bool:
    normalized = relative_path.replace("\\", "/").lower()
    return "/migrations/" in f"/{normalized}" or "migration" in Path(normalized).name


def _csharp_attributes_before(lines: list[str], line_start: int) -> list[str]:
    attrs: list[str] = []
    index = max(0, line_start - 2)
    while index >= 0:
        stripped = lines[index].strip()
        if not stripped:
            index -= 1
            continue
        if stripped.startswith("["):
            attrs.insert(0, stripped)
            index -= 1
            continue
        break
    return attrs


def _csharp_routes_from_attrs(attrs: list[str], *, class_route: bool) -> list[str]:
    routes: list[str] = []
    for attr in attrs:
        for match in CS_ROUTE_ATTR_RE.finditer(attr):
            attr_name = match.group("attr").lower()
            route = str(match.group("route") or "").strip("/")
            if class_route and attr_name == "route" and route:
                routes.append(route)
            elif not class_route and attr_name.startswith("http"):
                routes.append(route)
    return routes


def _combine_csharp_routes(prefixes: list[str], routes: list[str]) -> list[str]:
    if not routes:
        return []
    if not prefixes:
        return [route.strip("/") for route in routes]
    combined: list[str] = []
    for prefix in prefixes:
        for route in routes:
            combined.append("/".join(part.strip("/") for part in (prefix, route) if part.strip("/")))
    return combined


def _csharp_parent_class(class_infos: list[dict[str, Any]], line_start: int) -> dict[str, Any] | None:
    candidates = [item for item in class_infos if int(item["line_start"]) <= line_start <= int(item["line_end"])]
    if candidates:
        return sorted(candidates, key=lambda item: int(item["line_start"]), reverse=True)[0]
    previous = [item for item in class_infos if int(item["line_start"]) <= line_start]
    return sorted(previous, key=lambda item: int(item["line_start"]), reverse=True)[0] if previous else None


def _csharp_symbol_line_end(lines: list[str], line_start: int) -> int:
    if not lines:
        return 1
    balance = 0
    seen_open = False
    for index in range(max(0, line_start - 1), len(lines)):
        line = lines[index]
        balance += line.count("{")
        if "{" in line:
            seen_open = True
        balance -= line.count("}")
        if seen_open and balance <= 0:
            return index + 1
    return min(len(lines), max(line_start, line_start + 20))


def _frontend_component_name(raw_text: str, relative_path: str, language: str) -> str:
    if language == "vue":
        match = re.search(r"\bname\s*:\s*['\"](?P<name>[A-Za-z_][A-Za-z0-9_]*)['\"]", raw_text)
        if match:
            return match.group("name")
    return Path(relative_path).stem.replace(".module", "")


def _html_selector_references(raw_text: str, *, language: str, relative_path: str, source_symbol: str) -> list[CodeReference]:
    references: list[CodeReference] = []
    seen: set[str] = set()
    for match in HTML_CLASS_RE.finditer(raw_text):
        for value in str(match.group("value") or "").split():
            target = f".{value}"
            if target in seen:
                continue
            seen.add(target)
            references.append(
                CodeReference(
                    relationship_kind="selector",
                    target=target,
                    language=language,
                    path=relative_path,
                    line_start=raw_text[: match.start()].count("\n") + 1,
                    line_end=raw_text[: match.start()].count("\n") + 1,
                    chunk_index=0,
                    source_symbol=source_symbol,
                )
            )
    for match in HTML_ID_RE.finditer(raw_text):
        target = f"#{match.group('value')}"
        if target in seen:
            continue
        seen.add(target)
        references.append(
            CodeReference(
                relationship_kind="selector",
                target=target,
                language=language,
                path=relative_path,
                line_start=raw_text[: match.start()].count("\n") + 1,
                line_end=raw_text[: match.start()].count("\n") + 1,
                chunk_index=0,
                source_symbol=source_symbol,
            )
        )
    return references


def _result(
    chunks: list[CodeChunk],
    symbols: list[CodeSymbol] | tuple[CodeSymbol, ...],
    references: list[CodeReference] | tuple[CodeReference, ...],
    *,
    language: str,
    parser_status: str,
    generated: bool = False,
) -> CodeIndexResult:
    metadata: dict[str, Any] = {
        "language": language,
        "parser_status": parser_status,
        "symbol_count": len(symbols),
        "reference_count": len(references),
    }
    if generated:
        metadata["generated"] = True
    return CodeIndexResult(
        chunks=_mark_generated_chunks(tuple(chunks), generated=generated),
        symbols=_mark_generated_symbols(tuple(symbols), generated=generated),
        references=_mark_generated_references(tuple(references), generated=generated),
        metadata=metadata,
    )


def _chunk_from_lines(
    lines: list[str],
    *,
    title: str,
    relative_path: str,
    language: str,
    symbol_kind: str,
    primary_symbol: str,
    relationship: str,
    line_start: int,
    line_end: int,
    chunk_index: int,
    extra: dict[str, Any] | None = None,
) -> CodeChunk:
    body = "\n".join(lines[max(0, line_start - 1) : max(line_start, line_end)])
    return _chunk(
        body,
        title=title,
        relative_path=relative_path,
        language=language,
        symbol_kind=symbol_kind,
        primary_symbol=primary_symbol,
        relationship=relationship,
        line_start=line_start,
        line_end=line_end,
        chunk_index=chunk_index,
        extra=extra,
    )


def _chunk(
    text: str,
    *,
    title: str,
    relative_path: str,
    language: str,
    symbol_kind: str,
    primary_symbol: str,
    relationship: str,
    line_start: int,
    line_end: int,
    chunk_index: int,
    parser_status: str = "parsed",
    extra: dict[str, Any] | None = None,
) -> CodeChunk:
    redacted, _ = redact_text(text)
    redacted = EMAIL_RE.sub("[REDACTED:email]", redacted)
    metadata: dict[str, Any] = {
        "code": {
            "language": language,
            "primary_symbol": primary_symbol,
            "symbol_kind": symbol_kind,
            "relationship": relationship,
            "range": {"line_start": int(line_start), "line_end": int(line_end)},
            "parser_status": parser_status,
            "source_path": relative_path,
        },
        "language": language,
        "symbol_kind": symbol_kind,
        "relationship": relationship,
        "parser_status": parser_status,
    }
    if extra:
        metadata["code"].update(extra)
        metadata.update(extra)
    return CodeChunk(
        chunk_index=chunk_index,
        title=title,
        body=redacted.strip(),
        locator=f"line:{line_start}-{line_end}",
        token_estimate=max(1, len(redacted.split())),
        metadata=metadata,
    )


def _is_generated_file(raw_text: str) -> bool:
    head = "\n".join(raw_text.splitlines()[:20])
    return bool(GENERATED_MARKER_RE.search(head))


def _mark_generated_chunks(chunks: tuple[CodeChunk, ...], *, generated: bool) -> tuple[CodeChunk, ...]:
    if not generated:
        return chunks
    marked: list[CodeChunk] = []
    for chunk in chunks:
        code = dict(chunk.metadata.get("code") or {})
        code["generated"] = True
        marked.append(replace(chunk, metadata={**chunk.metadata, "generated": True, "code": code}))
    return tuple(marked)


def _mark_generated_symbols(symbols: tuple[CodeSymbol, ...], *, generated: bool) -> tuple[CodeSymbol, ...]:
    if not generated:
        return symbols
    return tuple(replace(symbol, metadata={**symbol.metadata, "generated": True}) for symbol in symbols)


def _mark_generated_references(references: tuple[CodeReference, ...], *, generated: bool) -> tuple[CodeReference, ...]:
    if not generated:
        return references
    return tuple(replace(reference, metadata={**reference.metadata, "generated": True}) for reference in references)


